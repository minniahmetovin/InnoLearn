package task03.lesson;

public class UnaryOps {
    public static void main(String[] args) {
        int a = 1;
        int b = 1;
        System.out.println(a++); //инкремент
        System.out.println(++b); //инкремент
        System.out.println(a--); //декремент
        System.out.println(--b); //декремент
    }
}
